package sample.model;

public class ConnectedProduct {

   private int connectedID;
   private int connectedProductID;
   private int subProductID;
    private int subQuantity;

    public int getConnectedID() {
        return connectedID;
    }

    public void setConnectedID(int connectedID) {
        this.connectedID = connectedID;
    }

    public int getConnectedProductID() {
        return connectedProductID;
    }

    public void setConnectedProductID(int connectedProductID) {
        this.connectedProductID = connectedProductID;
    }

    public int getSubProductID() {
        return subProductID;
    }

    public void setSubProductID(int subProductID) {
        this.subProductID = subProductID;
    }

    public int getSubQuantity() {
        return subQuantity;
    }

    public void setSubQuantity(int subQuantity) {
        this.subQuantity = subQuantity;
    }





}
